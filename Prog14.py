#for loop using break:
fruits = ["apple", "banana", "cherry"]
for xy in fruits:
  print(xy)
  if xy == "banana":
    break

#---
for x in fruits:
  if x == "banana":
    break
  print(x)
#using continue
for x in fruits:
  if x == "banana":
    continue
  print(x)